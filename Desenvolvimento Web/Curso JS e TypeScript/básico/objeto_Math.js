let num1 = 9.54356

//Arredondando para baixo
console.log(Math.floor(num1))

//Arredondando para cima
console.log(Math.ceil(num1))

//Arredondando para o mais próximo
console.log(Math.round(num1))

//Retorna o maior valor da sequencia
console.log(Math.max(1,2,3,4,5,10,0,-1,-4))

//Retorna o menor valor da sequencia
console.log(Math.min(1,2,3,4,5,10,0,-1,-4))

//Gerar números aleatórios (gera numero aleatório entre 0 e 1. O número 1 nunca será incluído)
console.log(Math.random())

//Valor de PI
console.log(Math.PI)

//Potenciação (2 elevado a 10)
console.log(Math.pow(2, 10))

//tbm posso elevar assim:
console.log(2 ** 10)